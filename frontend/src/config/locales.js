export const LOCALE_OPTIONS = [
  { code: "en", label: "English" },
  { code: "ur", label: "Urdu" },
  { code: "roman-urdu", label: "Roman Urdu" },
];

export const VOICE_OPTIONS = [
  { code: "en", label: "English speech" },
  { code: "ur", label: "Urdu speech" },
  { code: "bn", label: "Bangla speech" },
  { code: "sw", label: "Swahili speech" },
];

export const COUNTRY_LOCALE_OPTIONS = {
  GHA: ["en"],
  PAK: ["en", "ur", "roman-urdu"],
  KEN: ["en"],
  BGD: ["en"],
};

export const COUNTRY_VOICE_OPTIONS = {
  GHA: ["en"],
  PAK: ["en", "ur"],
  KEN: ["en", "sw"],
  BGD: ["en", "bn"],
};

const COUNTRY_SPEECH_TAGS = {
  GHA: { en: "en-GH" },
  PAK: { en: "en-PK", ur: "ur-PK" },
  KEN: { en: "en-KE", sw: "sw-KE" },
  BGD: { en: "en-BD", bn: "bn-BD" },
};

const DEFAULT_SPEECH_TAGS = {
  en: "en-US",
  ur: "ur-PK",
  bn: "bn-BD",
  sw: "sw-KE",
};

export function getLocaleLabel(code) {
  return LOCALE_OPTIONS.find((item) => item.code === code)?.label || "English";
}

export function getVoiceLabel(code) {
  return VOICE_OPTIONS.find((item) => item.code === code)?.label || "English speech";
}

export function getCountryLocaleOptions(countryCode) {
  const allowed = COUNTRY_LOCALE_OPTIONS[String(countryCode || "").toUpperCase()] || ["en"];
  return LOCALE_OPTIONS.filter((item) => allowed.includes(item.code));
}

export function getCountryVoiceOptions(countryCode) {
  const allowed = COUNTRY_VOICE_OPTIONS[String(countryCode || "").toUpperCase()] || ["en"];
  return VOICE_OPTIONS.filter((item) => allowed.includes(item.code));
}

export function resolveSupportedLocale(countryCode, desiredLocale) {
  const allowed = COUNTRY_LOCALE_OPTIONS[String(countryCode || "").toUpperCase()] || ["en"];
  return allowed.includes(desiredLocale) ? desiredLocale : allowed[0];
}

export function resolveSupportedVoiceLocale(countryCode, desiredLocale) {
  const allowed = COUNTRY_VOICE_OPTIONS[String(countryCode || "").toUpperCase()] || ["en"];
  return allowed.includes(desiredLocale) ? desiredLocale : allowed[0];
}

export function getSpeechRecognitionTag(countryCode, voiceLocale) {
  const normalizedCode = String(countryCode || "").toUpperCase();
  return COUNTRY_SPEECH_TAGS[normalizedCode]?.[voiceLocale] || DEFAULT_SPEECH_TAGS[voiceLocale] || "en-US";
}

export function getVoiceGuidance(locale, voiceLocale) {
  if (locale === "roman-urdu" && voiceLocale === "ur") {
    return "Voice works best if the user speaks natural Urdu. The transcript may appear in Urdu script, while the UI stays in Roman Urdu.";
  }
  if (voiceLocale === "bn") {
    return "Voice works best if the user speaks clear Bangla. Browser support varies, and the transcript may appear in Bangla script.";
  }
  if (voiceLocale === "sw") {
    return "Voice works best if the user speaks clear Swahili phrases. Browser support varies by device and browser.";
  }
  return "Voice input works best in Chrome or Edge and needs microphone permission.";
}

